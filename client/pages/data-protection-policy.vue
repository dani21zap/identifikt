            <h1>DATA PROTECTION POLICY ECARTAPI.COM</h1>
            <p><br></p>
            <p dir="ltr" class="description-paragraph description">The  Data  Protection  Policy  (hereinafter  "DPP")  governs  the  receipt,  storage,  usage, transfer, and disposal of Information, including the data vended and retrieved through the EcartAPI  Services  API.  This  DPP  is  applicable  to  all  systems  that  store,  process,  or otherwise  handle  data  vended  and  retrieved  from  the  EcartAPI  service.  This  DPP supplements  the  ECARTAPI.COM  Terms  and  Conditions  (“Terms”).  Failure  to  comply may result in suspension or termination of EcartAPI Services API access.</p>
